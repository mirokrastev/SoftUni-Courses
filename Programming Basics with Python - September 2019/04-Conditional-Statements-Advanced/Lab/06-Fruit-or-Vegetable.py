item = input()

if item == 'banana' \
        or item == 'apple' \
        or item == 'kiwi' \
        or item == 'cherry' \
        or item == 'lemon' \
        or item == 'grapes':
    print('fruit')
elif item == 'tomato' \
    or item == 'pepper' \
    or item == 'cucumber' \
    or item == 'carrot':
    print('vegetable')
else:
    print('unknown')